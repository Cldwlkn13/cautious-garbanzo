        public static MatchbookEvent ApplyTicks(this MatchbookEvent matchbookEvent)
        {
            foreach(var market in matchbookEvent.Markets)
            {
                foreach(var runner in market.Runners)
                {
                    foreach(var price in runner.Prices)
                    {
                        price.Tick = PriceToTick[price.Odds];
                    }
                }
            }

            return matchbookEvent;
        }


